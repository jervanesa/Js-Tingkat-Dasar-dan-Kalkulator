// Menambahkan event listener untuk menangani peristiwa saat dokumen HTML telah sepenuhnya dimuat
document.addEventListener("DOMContentLoaded", function() {
    
    // Mendapatkan elemen h1 dengan id "myH1"
    var h1Element = document.getElementById('myH1');

    // Mengubah isi (innerHTML) dari elemen h1 menjadi 'JavaScript Aktif'
    h1Element.innerHTML = 'JavaScript Aktif';
  });
  
